package Operators;

public class Arithmetic {
    public static void main(String[] args) {
        int a = 44;
        int b = 66;
        int sum = a+b;
        int diff = a-b;
        int mul = a*b;
        int div = b/a;//it results quotient
        int rem = b%a;
        System.out.println("sum = " +sum+ "\ndifference = " +diff+" \nproduct = "+mul+" \ndivision = "+div+"\nremainder = "+rem);
    }
}
